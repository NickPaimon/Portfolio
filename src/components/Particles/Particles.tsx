const ParticlesContainer = () => {
  const [init, setInit] = useState(false);

  // this should be run only once per application lifetime
  useEffect(() => {
    initParticlesEngine(async (engine) => {
      // you can initiate the tsParticles instance (engine) here, adding custom shapes or presets
      // this loads the tsparticles package bundle, it's the easiest method for getting everything ready
      // starting from v2 you can add only the features you need reducing the bundle size
      //await loadAll(engine);
      //await loadFull(engine);
      await loadSlim(engine);
      //await loadBasic(engine);
    }).then(() => {
      setInit(true);
    });
  }, []);

  const particlesLoaded = async (container?: Container): Promise<void> => {
    console.log(container);
  };

  const options: ISourceOptions = useMemo(
    () => ({
      fpsLimit: 120,
      interactivity: {
        events: {
          onClick: {
            enable: true,
            mode: 'push',
          },
          onHover: {
            enable: true,
            mode: 'repulse',
          },
        },
        modes: {
          push: {
            quantity: 4,
          },
          repulse: {
            distance: 200,
            duration: 0.4,
          },
        },
      },
      particles: {
        color: {
          value: ['#ffffff', '#ffffff', '#ffffff'],
          // opacity: 0.4, // Example: shades of blue and purple
        },
        links: {
          color: 'rgba(255, 255, 255, 0.4)', // Softer link color
          distance: 150,
          enable: true,
          opacity: 0.4,
          width: 1,
        },
        move: {
          direction: 'none',
          enable: true,
          outModes: {
            default: 'out',
          },
          random: true, // More dynamic movement
          speed: 3, // Slightly faster
          straight: false,
        },
        number: {
          density: {
            enable: true,
            area: 800, // Adjust as needed
          },
          value: 50, // Reduced for cleaner look
        },
        opacity: {
          value: 0.2, // Higher opacity for better visibility
        },
        shape: {
          type: 'circle',
        },
        size: {
          value: { min: 2, max: 5 }, // Smaller size range for subtlety
        },
      },
      detectRetina: true,
    }),
    [],
  );

  if (init) {
    return (
      <div className="z-0">
        <Particles
          id="tsparticles"
          particlesLoaded={particlesLoaded}
          options={options}
        />
      </div>
    );
  }

  return <></>;
};

export default ParticlesContainer;
